monthlystr=function(data){
  #data=subset(scen$bd,scen$bd$date>="2007-10-01" & scen$bd$date<="2008-09-30")
  xx=data %>% group_by(date=floor_date(date, "month")) %>%
    summarize(stream=sum(et))
  xx=as.data.frame(xx)
  xx$years=year(xx$date)
  xx$months=month(xx$date)
  x2=aggregate(xx$stream,by=list(xx$months),FUN=mean)
  tmp=rbind(x2[10:12,],x2[1:9,])
  tmp
}
streams=function(a,b,c,d,e,f,g,h,i,j,k,l,m,n){
  flow.1=monthlystr(a)
  flow.2=monthlystr(b)
  flow.3=monthlystr(c)
  flow.4=monthlystr(d)
  flow.5=monthlystr(e)
  flow.6=monthlystr(f)
  flow.7=monthlystr(g)
  flow.8=monthlystr(h)
  flow.9=monthlystr(i)
  flow.10=monthlystr(j)
  flow.11=monthlystr(k)
  flow.12=monthlystr(l)
  flow.13=monthlystr(m)
  flow.14=monthlystr(n)
  flow=cbind(flow.1$x,flow.2$x,flow.3$x,flow.4$x,flow.5$x,flow.6$x,flow.7$x,flow.8$x,flow.9$x,flow.10$x,flow.11$x,flow.12$x,flow.13$x,flow.14$x)
  tmp=NULL
  for (i in 1:12){
    tmp[i]=mean(flow[i,])
  }
  tmp
}


